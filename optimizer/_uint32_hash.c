uint32_t uint32_hash(uint32_t key)
{
    const uint32_t c2 = 0x27d4eb2d;
    key = (key ^ 61) ^ (key >> 16);
    key = key + (key << 3);
    key = key ^ (key >> 4);
    key = key * c2;
    key = key ^ (key >> 15);
    return key;
}

